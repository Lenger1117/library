def count_partitions(n):
    # Создаем массив для хранения количества разбиений
    dp = [0] * (n + 1)
    dp[0] = 1  # Существует 1 способ разбиения 0 (пустая сумма)

    # Заполняем массив dp
    for i in range(1, n + 1):
        for j in range(i, n + 1):
            dp[j] += dp[j - i]

    return dp[n]

# Пример использования
N = int(input("Введите число N: "))
print(f"Количество способов записи числа {N} в виде суммы положительных чисел: {count_partitions(N)}")